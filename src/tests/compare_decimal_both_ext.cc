int compare(std::string& lhs, std::string& rhs) {
    bool error;

    auto lhs_decimal = Decimal(lhs, &error);
    assert(!error);

    auto rhs_decimal = Decimal(rhs, &error);
    assert(!error);

    if (lhs_decimal < rhs_decimal) {
        return -1;
    } else if (lhs_decimal == rhs_decimal) {
        return 0;
    } else {
        return 1;
    }
}

